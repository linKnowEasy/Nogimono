#import "MaterialIcon.h"
#import <CoreText/CoreText.h>

#define NGColorFromRGB(rgbValue,a) [UIColor colorWithRed:((float)((rgbValue & 0xFF0000) >> 16))/255.0 green:((float)((rgbValue & 0xFF00) >> 8))/255.0 blue:((float)(rgbValue & 0xFF))/255.0 alpha:a];

@interface MaterialIcon ()
@property (strong, nonatomic) NSMutableAttributedString *symbolAttributedString;
@property (nonatomic) CGFloat fontSize;
@end

@implementation MaterialIcon
+ (void)initialize {
    
    if (self == MaterialIcon.class) {
        
        NSURL *url = [[NSBundle mainBundle] URLForResource:@"material" withExtension:@"ttf"];
        
        CGDataProviderRef fontDataProvider = CGDataProviderCreateWithURL((__bridge CFURLRef)url);
        CGFontRef newFont = CGFontCreateWithDataProvider(fontDataProvider);
        CGDataProviderRelease(fontDataProvider);
        
        CFErrorRef error;
        CTFontManagerRegisterGraphicsFont(newFont, &error);
        CGFontRelease(newFont);
    }
}

+ (instancetype)iconWithCode:(NSString *)code fontSize:(CGFloat)fontSize {
    
    
    MaterialIcon *icon = [MaterialIcon new];
    icon.fontSize = fontSize;
    icon.symbolAttributedString = [NSMutableAttributedString.alloc initWithString:code attributes:@{NSFontAttributeName:[self iconFontWithSize:fontSize]}];
    
    return icon;
}

+ (UIImage*)ImageWithIcon:(NSString*)icon_code iconColor:(UIColor *)iconColor iconSize:(CGFloat)iconSize imageSize:(CGSize)imageSize {
    MaterialIcon *symbol = [MaterialIcon iconWithCode:icon_code fontSize:iconSize];
    [symbol addAttribute:NSForegroundColorAttributeName value:iconColor];
    
    return [symbol imageWithSize:imageSize];
}

+ (UIImage*)ImageWithIcon:(NSString*)icon_code hexColor:(int)iconColor iconSize:(CGFloat)iconSize imageSize:(CGSize)imageSize {
    
    UIColor *hexColor = NGColorFromRGB(iconColor, 1.0);
    
    return [MaterialIcon ImageWithIcon:icon_code iconColor:hexColor iconSize:iconSize imageSize:imageSize];
}


+ (UIFont *)iconFontWithSize:(CGFloat)size {
    UIFont *font = [UIFont fontWithName:@"material" size:size];
    return font;
}

- (UIImage *)imageWithSize:(CGSize)imageSize {
    UIGraphicsBeginImageContextWithOptions(imageSize, NO, [UIScreen.mainScreen scale]);
    
    [self.symbolAttributedString drawInRect:({
        CGSize iconSize = [self.symbolAttributedString size];
        CGFloat xOffset = (imageSize.width - iconSize.width) / 2.0;
        CGFloat yOffset = (imageSize.height - iconSize.height) / 2.0;
        CGRectMake(xOffset, yOffset, iconSize.width, iconSize.height);
    })];
    
    UIImage *iconImage = UIGraphicsGetImageFromCurrentImageContext();
    
    UIGraphicsEndImageContext();
    
    return iconImage;
}

- (UIImage *)image {
    return [self imageWithSize:CGSizeMake(self.fontSize, self.fontSize)];
}

- (void)addAttributes:(NSDictionary *)attributes {
    [self.symbolAttributedString addAttributes:attributes range:[self symbolAttributedStringRange]];
}

- (void)addAttribute:(NSString *)name value:(id)value {
    [self.symbolAttributedString addAttribute:name value:value range:[self symbolAttributedStringRange]];
}

- (id)attribute:(NSString *)attributeName {
    return [self.symbolAttributedString attributesAtIndex:0 effectiveRange:NULL];
}

- (NSRange)symbolAttributedStringRange {
    return NSMakeRange(0, [self.symbolAttributedString length]);
}

